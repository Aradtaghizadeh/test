from turtle import *
ht()
speed(10)
pensize(1)
pencolor('gold')
begin_fill()
fillcolor('gold')
circle(75, 180)
circle(150, 180)
end_fill()
begin_fill()
fillcolor('black')
pencolor('black')
circle(75, -180)
end_fill()
up()
goto(0, -150)
down()
begin_fill()
circle(-150, -180)
end_fill()
pencolor('gold')
up()
goto(0, -95)
down()
begin_fill()
fillcolor('gold')
circle(20)
end_fill()
up()
goto(0, 0)
down()
begin_fill()
circle(75, 180)
end_fill()
up()
goto(0, 100)
down()
pencolor('black')
begin_fill()
fillcolor('black')
circle(20)
end_fill()
up()
goto(0, 175)
down()
pencolor("black")
write("Yin and yang", font=('Times New Roman', '15', 'bold'), align='center')
